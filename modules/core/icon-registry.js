// Función helper para obtener un icono con clases personalizadas
function getIcon(name, className = '') {
    const icon = IconRegistry[name];
    if (!icon) {
        console.warn(`Icon "${name}" not found in IconRegistry`);
        return `<svg viewBox="0 0 24 24" class="${className}"><rect width="24" height="24" fill="none"/></svg>`;
    }
    
    // Inyectar clase personalizada en el SVG
    return icon.replace('<svg', `<svg class="${className}"`);
}

